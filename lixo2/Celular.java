public class Celular{

    public int preco;
    public String nome;
    public String marca;

    public void setPreco(int preco){
        this.Preco = preco;
        }
    public void setNome(String nome){
        this.Nome = nome;
        }
    public void setMarca(String marca){
        this.Marca = marca;
        }
    public int getPreco(){
        return preco;
        }
    public String getNome(){
        return nome;
        }
    public String getMarca(){
        return marca;
        }
}
